// ─── Helpers ────────────────────────────────────────────────

function hotnessScore(upvotes: number, downvotes: number, createdAt: Date, autoGenerated: boolean): number {
  const score = upvotes - downvotes;
  const ageHours = (Date.now() - createdAt.getTime()) / (1000 * 60 * 60);
  const hotness = score / Math.pow(ageHours + 2, 1.5);
  return autoGenerated ? hotness * 0.7 : hotness;
}

function normalizeTags(tags: string[]): string[] {
  return [...new Set(tags.map((t) => t.toLowerCase().trim()).filter(Boolean))];
}

function periodToDate(period: string): Date | null {
  const now = new Date();
  switch (period) {
    case 'day':
      return new Date(now.getTime() - 24 * 60 * 60 * 1000);
    case 'week':
      return new Date(now.getTime() - 7 * 24 * 60 * 60 * 1000);
    case 'month':
      return new Date(now.getTime() - 30 * 24 * 60 * 60 * 1000);
    default:
      return null;
  }
}

// Board-aware default sort for comments
function defaultCommentSort(boardId: string): 'time' | 'votes' | 'best_first' {
  switch (boardId) {
    case 'helpdesk':
      return 'best_first';
    case 'genelab':
    case 'ideas':
      return 'votes';
    default:
      return 'time';
  }
}

