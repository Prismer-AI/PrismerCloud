  async renameTag(tagId: string, newName: string, userId: string) {
    const normalized = newName
      .trim()
      .toLowerCase()
      .replace(/[^a-z0-9-]/g, '');
    if (!normalized) throw new Error('Invalid tag name');

    const existing = await prisma.iMCommunityTag.findUnique({ where: { name: normalized } });
    if (existing && existing.id !== tagId) throw new Error('Tag name already exists');

    const oldTag = await prisma.iMCommunityTag.findUnique({ where: { id: tagId } });
    if (!oldTag) throw new Error('Tag not found');
    const oldName = oldTag.name;

    const tag = await prisma.iMCommunityTag.update({
      where: { id: tagId },
      data: { name: normalized },
    });

    // Update the tags JSON array in all linked posts
    const postTags = await prisma.iMCommunityPostTag.findMany({ where: { tagId } });
    for (const pt of postTags) {
      const post = await prisma.iMCommunityPost.findUnique({ where: { id: pt.postId }, select: { tags: true } });
      if (post?.tags) {
        try {
          const tagsArr: string[] = Array.isArray(post.tags) ? post.tags : JSON.parse(post.tags as any);
          const updated = [...new Set(tagsArr.map((t) => (t === oldName ? normalized : t)))];
          await prisma.iMCommunityPost.update({ where: { id: pt.postId }, data: { tags: updated } });
        } catch {}
      }
    }

    return tag;
  }

  async mergeTags(sourceTagId: string, targetTagId: string, userId: string) {
    if (sourceTagId === targetTagId) throw new Error('Cannot merge tag into itself');

    const [source, target] = await Promise.all([
      prisma.iMCommunityTag.findUnique({ where: { id: sourceTagId } }),
      prisma.iMCommunityTag.findUnique({ where: { id: targetTagId } }),
    ]);
    if (!source || !target) throw new Error('Tag not found');

    const sourcePostTags = await prisma.iMCommunityPostTag.findMany({ where: { tagId: sourceTagId } });
    for (const pt of sourcePostTags) {
      const existing = await prisma.iMCommunityPostTag.findFirst({ where: { postId: pt.postId, tagId: targetTagId } });
      if (existing) {
        await prisma.iMCommunityPostTag.delete({ where: { id: pt.id } });
      } else {
        await prisma.iMCommunityPostTag.update({ where: { id: pt.id }, data: { tagId: targetTagId } });
      }
      const post = await prisma.iMCommunityPost.findUnique({ where: { id: pt.postId }, select: { tags: true } });
      if (post?.tags) {
        try {
          const tagsArr: string[] = Array.isArray(post.tags) ? post.tags : JSON.parse(post.tags as any);
          const updated = [...new Set(tagsArr.map((t) => (t === source.name ? target.name : t)))];
          await prisma.iMCommunityPost.update({ where: { id: pt.postId }, data: { tags: updated } });
        } catch {}
      }
    }

    const finalCount = await prisma.iMCommunityPostTag.count({ where: { tagId: targetTagId } });
    await prisma.iMCommunityTag.update({ where: { id: targetTagId }, data: { postCount: finalCount } });
    await prisma.iMCommunityTag.delete({ where: { id: sourceTagId } });

    return target;
  }

  async deleteTag(tagId: string, userId: string) {
    const tag = await prisma.iMCommunityTag.findUnique({ where: { id: tagId } });
    if (!tag) throw new Error('Tag not found');

    const postTags = await prisma.iMCommunityPostTag.findMany({ where: { tagId } });
    for (const pt of postTags) {
      const post = await prisma.iMCommunityPost.findUnique({ where: { id: pt.postId }, select: { tags: true } });
      if (post?.tags) {
        try {
          const tagsArr: string[] = Array.isArray(post.tags) ? post.tags : JSON.parse(post.tags as any);
          const updated = tagsArr.filter((t) => t !== tag.name);
          await prisma.iMCommunityPost.update({ where: { id: pt.postId }, data: { tags: updated } });
        } catch {}
      }
    }

    await prisma.iMCommunityPostTag.deleteMany({ where: { tagId } });
    await prisma.iMCommunityTag.delete({ where: { id: tagId } });

    return { deleted: true, name: tag.name };
  }

  // ==================== Stats ====================

  async getStats(): Promise<CommunityStats> {
    const now = new Date();
    const sevenDaysAgo = new Date(now.getTime() - 7 * 24 * 60 * 60 * 1000);
    const todayStart = new Date(now.getFullYear(), now.getMonth(), now.getDate());

    const [totalPosts, totalComments, postsToday, activeAuthorsRaw, trendingTags] = await Promise.all([
      prisma.iMCommunityPost.count({ where: { deletedAt: null } }),
      prisma.iMCommunityComment.count({ where: { deletedAt: null } }),
      prisma.iMCommunityPost.count({
        where: { deletedAt: null, createdAt: { gte: todayStart } },
      }),
      prisma.iMCommunityPost.findMany({
        where: { deletedAt: null, createdAt: { gte: sevenDaysAgo } },
        select: { authorId: true },
        distinct: ['authorId'],
      }),
      this.getTrendingTags(5),
    ]);

    return {
      totalPosts,
      totalComments,
      activeAuthors7d: activeAuthorsRaw.length,
      postsToday,
      trendingTags: trendingTags.map((t: { name: string; postCount: number }) => ({
        name: t.name,
        postCount: t.postCount,
      })),
    };
  }

  // ==================== Internal helpers ====================

  private async getPostTitleForTarget(
    targetType: string,
    targetId: string,
  ): Promise<{ postId: string; postTitle: string } | null> {
    if (targetType === 'post') {
      const post = await prisma.iMCommunityPost.findUnique({
        where: { id: targetId },
        select: { id: true, title: true },
      });
      return post ? { postId: post.id, postTitle: post.title } : null;
    }
    if (targetType === 'comment') {
      const comment = await prisma.iMCommunityComment.findUnique({
        where: { id: targetId },
        select: { postId: true },
      });
      if (!comment) return null;
      const post = await prisma.iMCommunityPost.findUnique({
        where: { id: comment.postId },
        select: { id: true, title: true },
      });
      return post ? { postId: post.id, postTitle: post.title } : null;
    }
    return null;
  }

  /** Push a structured notification to an Agent via IM messages */
  private async pushToAgent(agentId: string, payload: Record<string, any>) {
    try {
      await prisma.iMMessage.create({
        data: {
          conversationId: agentId,
          senderId: 'system',
          type: 'system',
          content: JSON.stringify(payload),
        },
      });
      log.info(`Agent push: ${payload.type} to agent=${agentId}`);
    } catch (err: any) {
      log.error(`Agent push failed: ${err.message}`);
    }
  }

  /** Upsert tag rows and create post-tag link records */
  private async linkTagsToPost(postId: string, tags: string[]) {
    for (const name of tags) {
      const tag = await prisma.iMCommunityTag.upsert({
        where: { name },
        create: { name, postCount: 1 },
        update: { postCount: { increment: 1 } },
      });

      await prisma.iMCommunityPostTag
        .create({
          data: { postId, tagId: tag.id },
        })
        .catch(() => {
          // Unique constraint — link already exists, ignore
        });
    }
  }

  /** Remove post-tag links and decrement tag counts */
  private async unlinkTagsFromPost(postId: string, tags: string[]) {
    if (tags.length === 0) return;

    const existingLinks = await prisma.iMCommunityPostTag.findMany({
      where: { postId },
    });

    for (const link of existingLinks) {
      await prisma.iMCommunityPostTag.delete({ where: { id: link.id } }).catch(() => {});
      await prisma.iMCommunityTag
        .update({
          where: { id: link.tagId },
          data: { postCount: { decrement: 1 } },
        })
        .catch(() => {});
    }
  }
