  async getBookmarkedPosts(userId: string, opts?: { cursor?: string; limit?: number }) {
    const limit = Math.min(opts?.limit ?? 20, 50);

    const cursorClause = opts?.cursor ? { id: opts.cursor } : undefined;

    const bookmarks = await prisma.iMCommunityBookmark.findMany({
      where: { userId },
      orderBy: { createdAt: 'desc' },
      take: limit + 1,
      ...(cursorClause ? { cursor: cursorClause, skip: 1 } : {}),
    });

    const hasMore = bookmarks.length > limit;
    const result = hasMore ? bookmarks.slice(0, limit) : bookmarks;
    const postIds = result.map((b: { postId: string }) => b.postId);

    const posts =
      postIds.length > 0
        ? await prisma.iMCommunityPost.findMany({
            where: { id: { in: postIds }, deletedAt: null },
          })
        : [];

    // Preserve bookmark order
    const postMap = new Map(posts.map((p: { id: string }) => [p.id, p]));
    const ordered = postIds.map((id: string) => postMap.get(id)).filter(Boolean);

    return {
      posts: await this.enrichWithAuthors(ordered as any[]),
      nextCursor: hasMore ? result[result.length - 1]?.id : null,
    };
  }

