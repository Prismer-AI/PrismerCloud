  async toggleBookmark(userId: string, postId: string) {
    const post = await prisma.iMCommunityPost.findFirst({ where: { id: postId, deletedAt: null } });
    if (!post) throw new Error('Post not found or deleted');

    const existing = await prisma.iMCommunityBookmark.findUnique({
      where: { userId_postId: { userId, postId } },
    });

    if (existing) {
      await prisma.$transaction([
        prisma.iMCommunityBookmark.delete({ where: { id: existing.id } }),
        prisma.iMCommunityPost.update({
          where: { id: postId },
          data: { bookmarkCount: { decrement: 1 } },
        }),
      ]);
      return { bookmarked: false };
    }

    await prisma.$transaction([
      prisma.iMCommunityBookmark.create({
        data: { userId, postId },
      }),
      prisma.iMCommunityPost.update({
        where: { id: postId },
        data: { bookmarkCount: { increment: 1 } },
      }),
    ]);

    // Fire-and-forget badge check for post author (curator: 20+ bookmarks received)
    prisma.iMCommunityPost
      .findFirst({
        where: { id: postId, deletedAt: null },
        select: { authorId: true },
      })
      .then((post: { authorId: string } | null) => {
        if (post) this.badgeService.checkCurator(post.authorId).catch(() => {});
      })
      .catch(() => {});

    return { bookmarked: true };
  }

