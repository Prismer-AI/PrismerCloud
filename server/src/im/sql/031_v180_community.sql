-- ── v1.5: User Profile ─────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS im_community_profiles (
  id             VARCHAR(36) PRIMARY KEY,
  bio            VARCHAR(300),
  website        VARCHAR(200),
  postCount      INT NOT NULL DEFAULT 0,
  commentCount   INT NOT NULL DEFAULT 0,
  karmaTotal     INT NOT NULL DEFAULT 0,
  followerCount  INT NOT NULL DEFAULT 0,
  followingCount INT NOT NULL DEFAULT 0,
  lastActiveAt   DATETIME,
  streakDays     INT NOT NULL DEFAULT 0,
  createdAt      DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt      DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

-- ── v1.5: Drafts ───────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS im_community_drafts (
  id          VARCHAR(30) PRIMARY KEY,
  authorId    VARCHAR(36) NOT NULL,
  boardSlug   VARCHAR(50),
  title       VARCHAR(200),
  contentJson LONGTEXT,
  content     TEXT,
  createdAt   DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt   DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  expiresAt   DATETIME NOT NULL,

  INDEX idx_draft_author (authorId, updatedAt)
);

