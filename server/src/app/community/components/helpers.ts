export function timeAgo(ts: string): string {
  const diff = Date.now() - new Date(ts).getTime();
  const m = Math.floor(diff / 60000);
  if (m < 1) return 'just now';
  if (m < 60) return `${m}m ago`;
  const h = Math.floor(m / 60);
  if (h < 24) return `${h}h ago`;
  const d = Math.floor(h / 24);
  if (d < 30) return `${d}d ago`;
  return new Date(ts).toLocaleDateString();
}

/**
 * Strip markdown syntax from content for clean plain-text preview.
 * Removes headings, bold/italic, links, images, code blocks, hr, lists, blockquotes, and custom embeds.
 */
export function stripMarkdown(raw: string): string {
  return raw
    .replace(/\[\[gene:[^\]]+\]\]/g, '[Gene]')
    .replace(/\[\[skill:[^\]]+\]\]/g, '[Skill]')
    .replace(/```[\s\S]*?```/g, '')
    .replace(/`[^`]+`/g, '')
    .replace(/!\[.*?\]\(.*?\)/g, '')
    .replace(/\[([^\]]*)\]\(.*?\)/g, '$1')
    .replace(/^#{1,6}\s+/gm, '')
    .replace(/(\*\*|__)(.*?)\1/g, '$2')
    .replace(/(\*|_)(.*?)\1/g, '$2')
    .replace(/~~(.*?)~~/g, '$1')
    .replace(/^>\s+/gm, '')
    .replace(/^[-*+]\s+/gm, '')
    .replace(/^\d+\.\s+/gm, '')
    .replace(/^---+$/gm, '')
    .replace(/\n{2,}/g, ' ')
    .replace(/\n/g, ' ')
    .trim();
}

/**
 * Lightweight client-side markdown→HTML for expanded card fallback.
 * Handles tables, lists, headings, bold/italic, code, links, blockquotes.
 */
export function renderMarkdownClient(md: string): string {
  let html = md;

  html = html.replace(/```(\w*)\n([\s\S]*?)```/g, '<pre><code class="language-$1">$2</code></pre>');
  html = html.replace(/^### (.+)$/gm, '<h3>$1</h3>');
  html = html.replace(/^## (.+)$/gm, '<h2>$1</h2>');
  html = html.replace(/^# (.+)$/gm, '<h1>$1</h1>');
  html = html.replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>');
  html = html.replace(/\*(.+?)\*/g, '<em>$1</em>');
  html = html.replace(/~~(.+?)~~/g, '<del>$1</del>');
  html = html.replace(/`([^`]+)`/g, '<code>$1</code>');
  html = html.replace(/!\[([^\]]*)\]\(([^)]+)\)/g, '<img src="$2" alt="$1" />');
  html = html.replace(/\[([^\]]+)\]\(([^)]+)\)/g, '<a href="$2" target="_blank" rel="noopener noreferrer">$1</a>');
  html = html.replace(/^> (.+)$/gm, '<blockquote>$1</blockquote>');
  html = html.replace(/^---$/gm, '<hr />');

  const lines = html.split('\n');
  const out: string[] = [];
  let i = 0;
  while (i < lines.length) {
    const line = lines[i];
    if (/^\|.+\|/.test(line) && i + 1 < lines.length && /^\|[\s:|-]+\|/.test(lines[i + 1])) {
      const parseRow = (l: string) => l.replace(/^\|/, '').replace(/\|$/, '').split('|').map(c => c.trim());
      const hdr = parseRow(line);
      const aligns = parseRow(lines[i + 1]).map(c =>
        c.startsWith(':') && c.endsWith(':') ? 'center' : c.endsWith(':') ? 'right' : 'left'
      );
      let tbl = '<table><thead><tr>';
      hdr.forEach((h, j) => { tbl += `<th style="text-align:${aligns[j] ?? 'left'}">${h}</th>`; });
      tbl += '</tr></thead><tbody>';
      i += 2;
      while (i < lines.length && /^\|.+\|/.test(lines[i])) {
        const cells = parseRow(lines[i]);
        tbl += '<tr>';
        hdr.forEach((_, j) => { tbl += `<td style="text-align:${aligns[j] ?? 'left'}">${cells[j] ?? ''}</td>`; });
        tbl += '</tr>';
        i++;
      }
      tbl += '</tbody></table>';
      out.push(tbl);
      continue;
    }
    if (/^[-*]\s+/.test(line)) {
      const items: string[] = [line.replace(/^[-*]\s+/, '')];
      i++;
      while (i < lines.length && /^[-*]\s+/.test(lines[i])) { items.push(lines[i].replace(/^[-*]\s+/, '')); i++; }
      out.push(`<ul>${items.map(t => `<li>${t}</li>`).join('')}</ul>`);
      continue;
    }
    if (/^\d+\.\s+/.test(line)) {
      const items: string[] = [line.replace(/^\d+\.\s+/, '')];
      i++;
      while (i < lines.length && /^\d+\.\s+/.test(lines[i])) { items.push(lines[i].replace(/^\d+\.\s+/, '')); i++; }
      out.push(`<ol>${items.map(t => `<li>${t}</li>`).join('')}</ol>`);
      continue;
    }
    out.push(line);
    i++;
  }

  return out.join('\n')
    .split(/\n\n+/)
    .map(p => {
      const t = p.trim();
      if (!t) return '';
      if (/^<(h[1-6]|pre|blockquote|hr|ul|ol|table)/.test(t)) return t;
      return `<p>${t.replace(/\n/g, '<br />')}</p>`;
    })
    .filter(Boolean)
    .join('\n');
}

// Auth headers for community API calls
export function getCommunityAuthHeaders(): Record<string, string> {
  const headers: Record<string, string> = { 'Content-Type': 'application/json' };
  try {
    const authStored = localStorage.getItem('prismer_auth');
    if (authStored) {
      const authData = JSON.parse(authStored);
      if (authData.token && authData.expiresAt > Date.now()) {
        headers['Authorization'] = `Bearer ${authData.token}`;
        return headers;
      }
    }
    const apiKeyStored = localStorage.getItem('prismer_active_api_key');
    if (apiKeyStored) {
      const keyData = JSON.parse(apiKeyStored);
      if (keyData.key && keyData.status === 'ACTIVE') {
        headers['Authorization'] = `Bearer ${keyData.key}`;
        return headers;
      }
    }
  } catch {}
  return headers;
}

