const COMMUNITY_API = '/api/im/community';

function extractError(data: { error?: string | { code?: string; message?: string } }, fallback: string): string {
  if (!data.error) return fallback;
  if (typeof data.error === 'string') return data.error;
  if (typeof data.error === 'object' && data.error.message) return data.error.message;
  return fallback;
}

export async function fetchPosts(params: {
  tag?: string;
  authorId?: string;
  sort?: string;
  authorType?: string;
  cursor?: string;
  page?: number;
  limit?: number;
  boardSlug?: string;
}): Promise<{ posts: CommunityPost[]; nextCursor: string | null }> {
  const searchParams = new URLSearchParams();
  if (params.sort) searchParams.set('sort', params.sort);
  if (params.authorType && params.authorType !== 'all') searchParams.set('authorType', params.authorType);
  if (params.cursor) searchParams.set('cursor', params.cursor);
  if (params.page != null) searchParams.set('page', String(params.page));
  if (params.limit) searchParams.set('limit', String(params.limit));
  if (params.tag) searchParams.set('tag', params.tag);
  if (params.authorId) searchParams.set('authorId', params.authorId);
  if (params.boardSlug && params.boardSlug !== 'all') searchParams.set('boardSlug', params.boardSlug);
  const res = await fetch(`${COMMUNITY_API}/posts?${searchParams}`);
  const data = await res.json();
  if (!data.ok) throw new Error(extractError(data, 'Failed to fetch posts'));
  return data.data;
}

/** Extract the first image URL from markdown content */
export function extractFirstImage(content: string): string | null {
  const mdMatch = content.match(/!\[.*?\]\((https?:\/\/[^\s)]+)\)/);
  if (mdMatch) return mdMatch[1];
  const htmlMatch = content.match(/<img[^>]+src=["'](https?:\/\/[^\s"']+)["']/);
  if (htmlMatch) return htmlMatch[1];
  return null;
}

