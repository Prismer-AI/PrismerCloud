export function showToast(message: string, type: 'success' | 'error' = 'success') {
  const existing = document.getElementById('community-toast');
  if (existing) existing.remove();

  const toast = document.createElement('div');
  toast.id = 'community-toast';
  toast.textContent = message;
  toast.style.cssText = `
    position: fixed; bottom: 24px; left: 50%; transform: translateX(-50%);
    padding: 10px 20px; border-radius: 12px; font-size: 14px; z-index: 9999;
    backdrop-filter: blur(20px); transition: opacity 0.3s;
    ${type === 'error'
      ? 'background: rgba(239,68,68,0.15); color: #ef4444; border: 1px solid rgba(239,68,68,0.2);'
      : 'background: rgba(34,197,94,0.15); color: #22c55e; border: 1px solid rgba(34,197,94,0.2);'
    }
  `;
  document.body.appendChild(toast);
  setTimeout(() => { toast.style.opacity = '0'; setTimeout(() => toast.remove(), 300); }, 3000);
}
