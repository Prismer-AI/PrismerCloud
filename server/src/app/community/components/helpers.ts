export async function fetchNotifications(params?: {
  unread?: boolean;
  limit?: number;
  offset?: number;
}): Promise<{ notifications: CommunityNotification[]; total: number }> {
  const searchParams = new URLSearchParams();
  if (params?.unread) searchParams.set('unread', 'true');
  if (params?.limit != null) searchParams.set('limit', String(params.limit));
  if (params?.offset != null) searchParams.set('offset', String(params.offset));
  const res = await fetch(`${COMMUNITY_API}/notifications?${searchParams}`, {
    headers: getCommunityAuthHeaders(),
  });
  const data = await res.json();
  if (!data.ok) throw new Error(extractError(data, 'Failed to fetch notifications'));
  const d = data.data as { items: CommunityNotification[]; total: number };
  return { notifications: d.items, total: d.total };
}

