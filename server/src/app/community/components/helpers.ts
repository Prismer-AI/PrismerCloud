export async function searchTags(query: string, limit: number = 10): Promise<TrendingTag[]> {
  const searchParams = new URLSearchParams({ q: query, limit: String(limit) });
  try {
    const res = await fetch(`${COMMUNITY_API}/tags/search?${searchParams}`);
    const data = await res.json();
    if (data.ok) return data.data;
  } catch { /* fall through */ }
  const all = await fetchTrendingTags(50);
  const q = query.toLowerCase();
  return all.filter((t) => t.name.toLowerCase().includes(q)).slice(0, limit);
}

